// The legacy 'expired' status was removed in plan 260611-1000; the past-TTL
// state is now 'stale' (non-terminal, cascade-closeable in 1 step). This set
// mirrors TERMINAL_STATUSES in core/meta-state.js plus 'stale' for sweep's
// skip-terminal-or-stale iteration.
const TERMINAL_STATUSES = new Set(["auto-resolved", "resolved", "superseded", "stale"]);

/**
 * Check if an entry is past its staleness window.
 * Returns "stale" if `status: "active"` and last update is older than STALENESS_WINDOW_MS.
 * Returns null otherwise.
 *
 * Two stale paths:
 *   1. status: "reported" past expires_at -> "stale" (handled by checkExpiry in core/meta-state.js)
 *   2. status: "active" past STALENESS_WINDOW_MS -> "stale" (this function, NEW)
 *
 * The file-modification case (active finding whose evidence_code_ref file mtime
 * > last_verified_at) is preserved as auto-resolve; not part of this function.
 */
function checkStaleness(entry) {
  if (entry.entry_kind !== "finding") return null;
  if (entry.status !== "active") return null;
  const referenceTime = entry.acked_at || entry.created_at;
  if (!referenceTime) return null;
  const windowMs = Number(process.env.META_STATE_STALENESS_WINDOW_MS) || STALENESS_WINDOW_MS;
  const age = Date.now() - new Date(referenceTime).getTime();
  if (age > windowMs) return "stale";
  return null;
}

/**
 * Emit docs/registry-summary.md from the computed summary.
 * Overwrites any existing content (auto-generated, never edited by humans).
 */
function emitRegistrySummaryMd(root, entries, summary) {
  const docsDir = join(root, "docs");
  mkdirSync(docsDir, { recursive: true });
  const outPath = join(docsDir, "registry-summary.md");

  let md = "<!-- Auto-generated by meta_state_sweep. Do not edit. -->\n\n";
  md += `# Registry Summary\n\n`;
  md += `**Generated:** ${summary.last_generated_at}\n\n`;

  // Section: Counts
  md += `## Counts\n\n`;
  md += `| Kind | Status | Count |\n`;
  md += `|------|--------|-------|\n`;
  for (const [kind, statuses] of Object.entries(summary.counts)) {
    for (const [status, count] of Object.entries(statuses)) {
      md += `| ${kind} | ${status} | ${count} |\n`;
    }
  }
  md += `\n`;

  // Section: Coverage
  md += `## Coverage\n\n`;
  md += `- **Resolved findings:** ${summary.coverage.resolved_total}\n`;
  md += `- **With mechanism_check:** ${summary.coverage.mechanism_check_count} (${summary.coverage.mechanism_check_pct}%)\n`;
  md += `- **Broken refs:** ${summary.coverage.broken_refs}\n`;
  md += `- **Orphan findings:** ${summary.coverage.orphan_count}\n\n`;

  // Section: Top References
  md += `## Top References\n\n`;
  md += `| ID | Citations |\n`;
  md += `|----|-----------|\n`;
  for (const ref of summary.top_references) {
    md += `| ${ref.id} | ${ref.count} |\n`;
  }
  md += `\n`;

  // Section: Drift
  md += `## Drift\n\n`;
  md += `| ID | Status | Created At | Fingerprint |\n`;
  md += `|----|--------|------------|-------------|\n`;
  for (const entry of summary.drift) {
    md += `| ${entry.id} | ${entry.status} | ${entry.created_at} | ${entry.code_fingerprint || "—"} |\n`;
  }

  // Section: Stale Findings
  md += `\n## Stale Findings\n\n`;
  md += `| ID | Status | Last Verified | Created At |\n`;
  md += `|----|--------|---------------|------------|\n`;
  const staleEntries = entries.filter((e) => e.status === "stale").slice(0, 10);
  for (const entry of staleEntries) {
    md += `| ${entry.id} | ${entry.status} | ${entry.last_verified_at || "—"} | ${entry.created_at} |\n`;
  }

  writeFileSync(outPath, md, "utf8");
  return outPath;
}

export const metaStateSweepTool = {
  name: "meta_state_sweep",
  description: "Walk the meta-state registry and propose (or apply) lifecycle transitions: expiry for reported entries past expires_at, auto-resolve for entries whose watched file was modified after creation. Dry-run by default. Operator-only (env: OPERATOR_MODE=1). CAS-safe via the version field. Use to keep the registry honest without manual per-entry work.",
  schema: {
    apply: z.union([z.boolean(), z.string()]).transform(strictBooleanGuard).optional().default(false).describe("If true, commit the transitions. Default false (dry-run)."),
  },
  handler: async ({ apply }) => {
    if (apply && process.env.OPERATOR_MODE !== "1" && process.env.OPERATOR_MODE !== "true") {
      return { content: [{ type: "text", text: JSON.stringify({ swept: false, reason: "operator_role_required" }) }] };
    }
    const root = resolveRoot();
    const entries = readRegistry(root);
    const entryById = new Map(entries.map((e) => [e.id, e]));
    const transitions = [];

    for (const entry of entries) {
      if (TERMINAL_STATUSES.has(entry.status)) continue;
      const fromCheckExpiry = checkExpiry(entry);
      const fromCheckStaleness = checkStaleness(entry);
      const targetStatus = fromCheckExpiry || fromCheckStaleness;
      if (targetStatus && targetStatus !== entry.status) {
        transitions.push({ id: entry.id, from: entry.status, to: targetStatus, expected_version: entry.version ?? 0 });
      }
    }

    if (apply) {
      const results = [];
      const staleReports = [];
      const sweepTimestamp = new Date().toISOString();
      for (const t of transitions) {
        const isStaleTransition = t.to === "stale";
        const patch = {
          status: t.to,
          _expected_version: t.expected_version,
        };
        if (!isStaleTransition) {
          patch.resolved_at = sweepTimestamp;
          patch.resolved_by = "auto-resolve";
        }
        const r = await updateEntry(root, t.id, patch);
        if (r === "version_mismatch") {
          results.push({ id: t.id, applied: false, reason: "version_mismatch" });
        } else if (r === true) {
          results.push({ id: t.id, applied: true, to: t.to });

          // Emit a follow-up report for each newly-stale entry so the
          // operator gets a structured prompt to triage (re-verify or resolve)
          // rather than discovering the staleness only via the Phase 6
          // cold-tier invariant. The follow-up uses mechanism_check=false so
          // that, when it later ages out, it does not contribute to the
          // stale-mc threshold itself.
          if (isStaleTransition) {
            const original = entryById.get(t.id);
            const evidenceSuffix = original?.evidence_code_ref
              ? ` (evidence_code_ref: ${original.evidence_code_ref})`
              : "";
            try {
              const reportResult = await metaStateReportTool.handler({
                category: "stale-ref",
                severity: "warning",
                affected_system: "meta-state-tools",
                description:
                  `Entry ${t.id}${evidenceSuffix} aged out and was transitioned ` +
                  `from active to stale by meta_state_sweep on ${sweepTimestamp} ` +
                  `because acked_at || created_at exceeded STALENESS_WINDOW_MS. ` +
                  `Operator action: re-verify via meta_state_re_verify if the ` +
                  `underlying issue is still relevant, or resolve via ` +
                  `meta_state_resolve if the fix already shipped.`,
                reopens: [t.id],
                mechanism_check: false,
              });
              const reported = JSON.parse(reportResult.content[0].text);
              staleReports.push({ original_id: t.id, follow_up_id: reported.id });
            } catch (err) {
              // Sweep must not fail because follow-up emission failed; log
              // and continue so other stale entries still get triaged.
              staleReports.push({ original_id: t.id, error: err.message || String(err) });
            }
          }
        }
      }
      appendGateLog(root, {
        timestamp: sweepTimestamp,
        tool: "meta_state_sweep",
        applied: results.length,
        results,
        stale_reports: staleReports,
      });

      // Emit registry-summary.md (Phase 7 of plan 260606)
      const summary = buildRegistrySummary(entries);
      emitRegistrySummaryMd(root, entries, summary);

      return {
        content: [
          {
            type: "text",
            text: JSON.stringify({ swept: true, results, stale_reports: staleReports }),
          },
        ],
      };
    }

    return {
      content: [
        {
          type: "text",
          text: JSON.stringify({
            swept: false,
            dry_run: true,
            transitions,
            summary_preview: buildRegistrySummary(entries),
          }),
        },
      ],
    };
  },
};
